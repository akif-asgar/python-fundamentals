
"""
Variables in Python
-------------------
Variables are containers for storing data values.
Main types: string, integer, float, boolean
"""

# String
name = "Akif"
food = "Pizza"

print(f"My name is {name} and I like {food}.")

# Integer
age = 25
print(f"My age is {age}.")

# Float
height = 1.75
print(f"My height is {height} meters.")

# Boolean
is_student = True
print(f"Am I a student? {is_student}.")