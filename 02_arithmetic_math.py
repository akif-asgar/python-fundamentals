import math

number = 16
a = 3.4
b = -2

# Arithmetic Operators

result = number + a # Addition // output: 19.4
result = number - a # Subtraction // output: 12.6
result = number * a # Multiplication // output: 54.4
result = number / a # Division // output: 4.705882352941177
result = number % 3 # Modulus // output: 1
result = number ** 2 # Exponentiation // output: 256
result = number // 3 # Floor Division // output: 5

# Built-in Functions

result = abs(b) # Absolute Value // output: 2
result = round(a) # Rounding // output: 3
result = round(a, 1) # Rounding to 1 decimal place // output: 3.4
result = max(number, a, b) # Maximum // output: 16
result = min(number, a, b) # Minimum // output: -2
result = sum([number, a, b]) # Sum // output: 17.4
result = pow(a, 2) # Power using built-in function // output: 11.559999999999999

# Math Functions

result = math.sqrt(number) # Square Root // output: 4.0
result = math.pow(number, 2) # Power // output: 256.0
result = math.ceil(a) # Ceiling // output: 4
result = math.floor(a) # Floor // output: 3
result = math.pi # Pi // output: 3.141592653589793
result = math.e # Euler's Number // output: 2.718281828459045
